package com.njm.oss.model.demo;

public class Prod {
	private int merchandise_id;
	private String merchandise_cd;
	private int purchase_id;
	private String merchandise_nm;
	private String description;
	private double retail_price;
	private String specifications;

	public int getMerchandise_id() {
		return merchandise_id;
	}

	public void setMerchandise_id(int merchandise_id) {
		this.merchandise_id = merchandise_id;
	}

	public String getMerchandise_cd() {
		return merchandise_cd;
	}

	public void setMerchandise_cd(String merchandise_cd) {
		this.merchandise_cd = merchandise_cd;
	}

	public int getPurchase_id() {
		return purchase_id;
	}

	public void setPurchase_id(int purchase_id) {
		this.purchase_id = purchase_id;
	}

	public String getMerchandise_nm() {
		return merchandise_nm;
	}

	public void setMerchandise_nm(String merchandise_nm) {
		this.merchandise_nm = merchandise_nm;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public double getRetail_price() {
		return retail_price;
	}

	public void setRetail_price(double retail_price) {
		this.retail_price = retail_price;
	}

	public String getSpecifications() {
		return specifications;
	}

	public void setSpecifications(String specifications) {
		this.specifications = specifications;
	}

}
